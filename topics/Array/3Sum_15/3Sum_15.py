# https://leetcode.com/problems/3sum/
# ---------------------------------------------------
from typing import List

# Runtime Complexity: O(N * log(N) + N^2)
# Space Complexity: O(1), if we don't count resulting list
class Solution:
    def threeSum(self, nums: List[int]) -> List[List[int]]:
        res_triplets = []

        nums.sort()

        n = len(nums)
        for i in range(n - 2):
            if i > 0 and nums[i] == nums[i - 1]:
                continue

            left, right = i + 1, n - 1
            while left < right:
                total_sum = nums[i] + nums[left] + nums[right]
                if total_sum == 0:
                    res_triplets.append([nums[i], nums[left], nums[right]])

                if total_sum <= 0:
                    left += 1
                    while left <= right and nums[left] == nums[left - 1]:
                        left += 1
                else:
                    right -= 1
                    while left <= right and nums[right] == nums[right + 1]:
                        right -= 1

        return res_triplets


